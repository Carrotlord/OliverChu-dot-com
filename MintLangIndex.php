<h1>Mint is a...</h1>

<table class="centerobject" style="width:75%">
<tr><td>
Dynamically typed general purpose programming language. It follows the Python <i>batteries included</i> philosophy, which means there are built-in libraries
for graphics, generating random numbers, file input/output, web uploading &amp; downloading, HTML page scraping, and interfacing with Internet Relay Chat.
Mint is <a class="important" href="https://github.com/Carrotlord/Mint-Language-Latest-Builds-Plus" target="_blank">open source</a>, <a class="important" href="https://www.google.com/search?q=Both+%22free+as+in+freedom+and+free+as+in+free+beer%22" target="_blank">free</a>, and <a class="important" href="http://en.wikipedia.org/wiki/List_of_operating_systems" target="_blank">cross-platform</a>. It focuses on useful syntax, ubiquitous code readability, and easily understandable recursive data structures.<br /><br />Self similarity is key to the language, and unification of different
programming ideas, data types, and ways of coding is a definite must. We support both object-oriented styles, such as that from Java, and functional styles, such as that from Haskell.
Both iteration and recursion are perfectly usable. Functions and classes are first class, and even better... functions and classes are the exact same thing, just like in JavaScript. Pointers can be referenced, dereferenced, and modified just like in C and C++. Even more, meta-circular-evaluation is easy to setup.<br /><br />

The Hello World program can be written in one or two lines of Mint code.
Because Mint programmers are never boring, the following two-line hello world script
will open a web browser that displays an image of tiny microorganisms spelling out Hello World on a nutrient rich biofilm.
How's that for a first program?

